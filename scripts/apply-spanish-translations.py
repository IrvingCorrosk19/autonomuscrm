def main():
    with open(EN_PATH, encoding="utf-8-sig") as f:
        en = json.load(f)
    with open(ES_PATH, encoding="utf-8-sig") as f:
        es = json.load(f)

    for k, v in SPANISH_MAP.items():
        if k in es:
            es[k] = v

    en.update(NEW_KEYS_EN)
    es.update(NEW_KEYS_ES)

    with open(EN_PATH, "w", encoding="utf-8") as f:
        json.dump(en, f, ensure_ascii=False, indent=4)
        f.write("\n")
    with open(ES_PATH, "w", encoding="utf-8") as f:
        json.dump(es, f, ensure_ascii=False, indent=4)
        f.write("\n")

    identical = sum(
        1 for k in en
        if k in es and en[k] == es[k] and any(c.isalpha() for c in str(en[k])) and len(str(en[k])) >= 4
    )
    print(f"Keys en={len(en)} es={len(es)} identical={identical}")


if __name__ == "__main__":
    main()
